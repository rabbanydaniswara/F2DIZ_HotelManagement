    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
     // TOMBOL SIMPAN
        String idReservasi = jTextField5.getText().trim();
        String totalStr = jTextField7.getText().trim();
        String pembayaran = (String) jComboBox1.getSelectedItem();
        Date tanggalTransaksi = jDateChooser3.getDate();
        String status = (String) jComboBox2.getSelectedItem();

        if (idReservasi.isEmpty() || totalStr.isEmpty() || tanggalTransaksi == null || 
            pembayaran == null || status == null) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }

        try {
            double total = Double.parseDouble(totalStr);

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tanggalFormatted = sdf.format(tanggalTransaksi);

            try (Connection conn = DatabaseConnection.connect()) {
                String sql = "INSERT INTO transaksi (id_reservasi, total, pembayaran, "
                        + "tanggal_transaksi, status_transaksi) " +
                             "VALUES (?, ?, ?, ?, ?)";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setInt(1, Integer.parseInt(idReservasi));
                stmt.setDouble(2, total);
                stmt.setString(3, pembayaran);
                stmt.setString(4, tanggalFormatted);
                stmt.setString(5, status);

                stmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan.");
                
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField6.setText("");
                jTextField5.setText("");
                jTextField7.setText("");
                jComboBox1.setSelectedIndex(-1);
                jComboBox2.setSelectedIndex(-1);
                jDateChooser3.setDate(null);
                tampilkanTransaksi();
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Total dan ID reservasi harus berupa angka.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton7ActionPerformed

