    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //TOMBOL PILIH
        try {
            Connection conn = config.DatabaseConnection.connect();

            String idTamu = jTextField1.getText();
            String nomorKamar = jTextField2.getText();

            String sql = "SELECT r.id_reservasi, r.tanggal_check_in, r.tanggal_check_out, k.harga_kamar " +
                         "FROM reservasi r JOIN kamar k ON r.nomor_kamar = k.nomor_kamar " +
                         "WHERE r.id_tamu = ? AND r.nomor_kamar = ?";

            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idTamu);
            stmt.setString(2, nomorKamar);

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                jTextField5.setText(rs.getString("id_reservasi"));

                java.sql.Date checkIn = rs.getDate("tanggal_check_in");
                java.sql.Date checkOut = rs.getDate("tanggal_check_out");
                long lama = (checkOut.getTime() - checkIn.getTime()) / (1000 * 60 * 60 * 24);
                jTextField6.setText(String.valueOf(lama+" Hari"));

                double harga = rs.getDouble("harga_kamar");
                double total = harga * lama;
                jTextField7.setText(String.valueOf(total));
            } else {
                JOptionPane.showMessageDialog(null, "Data reservasi tidak ditemukan!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Kesalahan database: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

