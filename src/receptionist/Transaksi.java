    private void tampilkanTamu() {
        try {
            Connection conn = config.DatabaseConnection.connect();
            String sql = "SELECT r.id_reservasi, r.nomor_kamar, r.id_tamu, t.nama_tamu " +
                 "FROM reservasi r JOIN tamu t ON r.id_tamu = t.id_tamu";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Reservasi");
            model.addColumn("Nomor Kamar");
            model.addColumn("ID Tamu");
            model.addColumn("Nama Tamu");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_reservasi"),
                    rs.getString("nomor_kamar"),
                    rs.getString("id_tamu"),
                    rs.getString("nama_tamu"),
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    
    private void tampilkanTransaksi() {
        try {
            Connection conn = config.DatabaseConnection.connect();
            String sql = "SELECT id_transaksi, id_reservasi, total, pembayaran, "
                    + "tanggal_transaksi, status_transaksi " +
                         "FROM transaksi";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Transaksi");
            model.addColumn("ID Reservasi");
            model.addColumn("Total");
            model.addColumn("Pembayaran");
            model.addColumn("Tanggal");
            model.addColumn("Status");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_transaksi"),
                    rs.getInt("id_reservasi"),
                    rs.getDouble("total"),
                    rs.getString("pembayaran"),
                    rs.getDate("tanggal_transaksi"),
                    rs.getString("status_transaksi")
                });
            }

            jTable2.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat menampilkan transaksi: " + e.getMessage());
        }

    }
